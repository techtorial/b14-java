package Primitives;

public class LogicalNegation {

    public static void main(String[] args) {


        boolean isLightOn = true;

        System.out.println(isLightOn); // true


        System.out.println( !isLightOn); //

        isLightOn = !isLightOn;

        System.out.println(isLightOn); //


    }
}
